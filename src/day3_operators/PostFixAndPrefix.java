package day3_operators;

public class PostFixAndPrefix {
	public static void main(String[] args) {
		
		int a = 1;
		
		
		// prefix => ++a = a+1
		System.out.println(++a);
		
		int b = 1; 
		
		
//		postfix +> b++ 
		System.out.println(b++); // 1
		
		System.out.println(b); // 2
		
		//--
		
		int c = 5;
		
		
		System.out.println(--c); //4
		
		System.out.println(c--); //4
		System.out.println(c); //3
		
		
		
		
		
		
		
	}
}
